    <script>
        const form = document.querySelector(".message__form");

        const fields = {
            fname: value => value.trim() !== "" || "Full name is required",
            email: value => /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(value) || "Enter valid email",
            "phone-number": value => /^[0-9+\s()-]{7,}$/.test(value) || "Enter valid phone number",
            subject: value => value.trim() !== "" || "Subject is required",
            message: value => value.trim() !== "" || "Message is required"
        };

        // Blur validation (when clicking next input)
        Object.keys(fields).forEach(id => {
            const input = document.getElementById(id);
            const error = input.nextElementSibling;

            input.addEventListener("blur", () => {
                validateInput(input, error, fields[id]);
            });

            // Remove error while typing
            input.addEventListener("input", () => {
                input.classList.remove("error-border");
                error.textContent = "";
            });
        });

        // Submit validation
        form.addEventListener("submit", e => {
            e.preventDefault();
            let isValid = true;

            Object.keys(fields).forEach(id => {
                const input = document.getElementById(id);
                const error = input.nextElementSibling;
                const result = fields[id](input.value);

                if (result !== true) {
                    showError(input, error, result);
                    isValid = false;
                }
            });

            if (isValid) {
                alert("Form submitted successfully!");
                form.reset();
            }
        });

        function validateInput(input, error, validator) {
            const result = validator(input.value);
            if (result !== true) {
                showError(input, error, result);
            }
        }

        function showError(input, error, message) {
            input.classList.add("error-border");
            error.textContent = message;
        }
    </script>



